-module(vixen_db).
-author("ausimian").

-include("vixen.hrl").

%%%=============================================================================
%%% Export and Defs
%%%=============================================================================

%% External API
-export([
    lookup/1
]).

%%%=============================================================================
%%% API
%%%=============================================================================
-spec lookup(atom()) -> vixen:handshake_pattern().
lookup('NN') -> [
    {ini, [e]},
    {rsp, [e, ee]}
];
lookup('XX') -> [
    {ini, [e]},
    {rsp, [e, ee, s, es]},
    {ini, [s, se]}
];
lookup('KK') -> [
    {ini, [s]},
    {rsp, [s]},
    ?SEP,
    {ini, [e, es, ss]},
    {rsp, [e, ee, se]}
].
